/**
 *   ======================= useNotifications.js =======================
 * üîî
 * MAIN HOOK: Real-time, in-memory notifications for Royal TV user.
 * ---------------------------------------------------------------------
 * - React hook for managing a user's notifications over sockets (no REST).
 * - Always expects server to emit: { notifications: [], unreadCount, total }
 * - Handles fetching, live pushes, marking as read, manual refresh, and sorting.
 * - Exposes helpers for slicing previews, paginated drawers, etc.
 * - Built for safety (future proof), performance, and user experience!
 * =====================================================================
 * ‚öôÔ∏è
 * USAGE:
 *   const {
 *     notifications, unreadCount, loading,
 *     markAsRead, refreshNotifications,
 *     getPreview, getDrawerSlice, resortNotifications
 *   } = useNotifications(userId);
 * =====================================================================
 */
import { useState, useEffect, useCallback } from 'react';
import useSocketHub from '@/hooks/socket/useSocketHub';

export default function useNotifications(userId) {
  // üéõÔ∏è Get all notification-related socket functions from your unified socket hub
  const {
    requestNotifications, // üîπ Ask server for all my notifications
    onNotificationsList, // üîπ Listen for full list (fetch/refresh)
    onNotificationReceived, // üîπ Listen for single real-time notification pushes
    markNotificationRead, // üîπ Ask server to mark a specific notification as read
    deleteNotification, // üîπ Ask server to delete single notification for user / admin
    clearNotifications, // üîπ Ask server to clear all notifications for user / admin
    onNotificationsError // üîπ Used For showing errors to user
  } = useSocketHub();

  // üì¶ Local state: full list, unread badge, loading spinner
  const [notifications, setNotifications] = useState([]); // All notifications (unread first)
  const [unreadCount, setUnreadCount] = useState(0); // Unread count for badge
  const [loading, setLoading] = useState(false); // Is loading (UI spinner)

  // ============================================================
  // 1Ô∏è‚É£ ON MOUNT / USER CHANGE: Fetch & listen for notifications
  // ------------------------------------------------------------
  // - When user logs in or changes, auto-fetch notifications from server.
  // - Listen for server's notifications_list event.
  // - Handles initial load, manual refresh, and server-initiated refreshes.
  // ============================================================
  useEffect(() => {
    if (!userId) return; // üõë No user, skip everything

    setLoading(true); // üö¶ Show loading spinner while waiting for server

    // ‚¨ÜÔ∏è Ask server for full list (triggers server to emit notifications_list)
    requestNotifications(userId);

    // üëÇ Listen for the "notifications_list" socket event
    // Server always emits: { notifications: [...], unreadCount, total }
    const stop = onNotificationsList((data) => {
      // üëÄ Defensive parsing: always expect an object, fallback to empty array
      let notificationArray = [];
      let unreadCountFromServer = 0;

      if (data && Array.isArray(data.notifications)) {
        // üü¢ Standard case: data.notifications is an array
        notificationArray = data.notifications;
        unreadCountFromServer = data.unreadCount || 0;
      } else if (data && Array.isArray(data)) {
        // üü° Fallback: server (old) sent a bare array instead of object
        notificationArray = data;
      } else {
        // üî¥ Totally empty/null/undefined: treat as empty
        notificationArray = [];
      }

      // üîÄ SORT: Show all unread first (newest first), then read (newest first)
      const unread = notificationArray
        .filter((n) => n && !n.is_read)
        .sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
      const read = notificationArray
        .filter((n) => n && n.is_read)
        .sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));

      // ‚úÖ Save to state: full list and unread badge count
      setNotifications([...unread, ...read]);
      setUnreadCount(unread.length);
      setLoading(false); // üéâ Done loading
    });

    // üßπ Cleanup: remove listener when component unmounts or userId changes
    return () => stop && stop();
  }, [userId, requestNotifications, onNotificationsList]);

  // ============================================================
  // 2Ô∏è‚É£ REAL-TIME PUSH HANDLER: New notification arrives!
  // ------------------------------------------------------------
  // - Listens for single new notifications (pushed by server in real-time).
  // - Prepends to array, avoids duplicates, keeps sorting.
  // ============================================================
  useEffect(() => {
    if (!userId) return;
    const stopPush = onNotificationReceived((newNotif) => {
      setNotifications((prev) => {
        // üîé If already in array (duplicate), skip!
        if (prev.some((n) => n.notification_id === newNotif.notification_id)) return prev;

        // ‚ûï Add to start, re-sort all (unread first, newest first)
        const merged = [newNotif, ...prev];
        const unread = merged
          .filter((n) => n && !n.is_read)
          .sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
        const read = merged
          .filter((n) => n && n.is_read)
          .sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
        setUnreadCount(unread.length); // üõéÔ∏è Update badge
        return [...unread, ...read];
      });
    });
    // üßπ Cleanup listener
    return () => stopPush && stopPush();
  }, [userId, onNotificationReceived]);

  // ============================================================
  // 3Ô∏è‚É£ MARK-AS-READ: User opens or reads a notification
  // ------------------------------------------------------------
  // - Optimistically updates local state instantly.
  // - Sends mark-as-read event to server (socket).
  // ============================================================
  const markAsRead = useCallback(
    (notification_id) => {
      if (!notification_id) return;
      markNotificationRead(notification_id); // üöÄ Tell server
      setNotifications((prev) =>
        prev.map((n) => (n.notification_id === notification_id ? { ...n, is_read: true } : n))
      );
      setUnreadCount((c) => Math.max(0, c - 1)); // üõéÔ∏è Lower badge count
    },
    [markNotificationRead]
  );

  // ============================================================
  // 4Ô∏è‚É£ MANUAL REFRESH: User clicks "Refresh Notifications"
  // ------------------------------------------------------------
  // - Triggers fetch as if mounting for first time (re-runs server emit).
  // ============================================================
  const refreshNotifications = useCallback(() => {
    if (!userId) return;
    setLoading(true); // üö¶ Show loading
    requestNotifications(userId); // üîÅ Server will send fresh list
  }, [userId, requestNotifications]);

  // üóëÔ∏è Delete single notification (by id)
  const removeNotification = useCallback(
    (notification_id) => {
      if (!userId || !notification_id) return;
      deleteNotification(notification_id, userId);
    },
    [deleteNotification, userId]
  );

  // üî• Clear all notifications for user
  const clearAllNotifications = useCallback(() => {
    if (!userId) return;
    clearNotifications(userId);
  }, [clearNotifications, userId]);

  // ============================================================
  // 5Ô∏è‚É£ HELPERS: Preview/drawer slicing for UI display
  // ------------------------------------------------------------
  // - getPreview(count): get top n notifications for "preview"
  // - getDrawerSlice(start, end): paginated slices for drawer or page
  // ============================================================
  const getPreview = useCallback(
    (count = 3) => (notifications || []).slice(0, count),
    [notifications]
  );
  const getDrawerSlice = useCallback(
    (startIdx, endIdx) => (notifications || []).slice(startIdx, endIdx),
    [notifications]
  );

  // ============================================================
  // 6Ô∏è‚É£ RESORT: Re-sort notifications (e.g., after collapsing a read)
  // ------------------------------------------------------------
  // - Can be called by UI after marking as read/collapsing to keep order
  // ============================================================
  const resortNotifications = useCallback(() => {
    setNotifications((prev) => {
      const unread = (prev || [])
        .filter((n) => n && !n.is_read)
        .sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
      const read = (prev || [])
        .filter((n) => n && n.is_read)
        .sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
      return [...unread, ...read];
    });
  }, []);

  useEffect(() => {
    const stop = onNotificationsError((error) => {
      // üõë Handle error: show toast, modal, or console
      console.error('Notification error:', error?.message || error);
      // Or update state for UI!
    });
    return () => stop && stop();
  }, [onNotificationsError]);
  // ============================================================
  // ‚úÖ EXPORT: Return all helpers for NotificationCenter, badges, etc.
  // ------------------------------------------------------------
  // - notifications: full sorted array
  // - unreadCount: for badges
  // - loading: for spinners
  // - markAsRead: call when user opens a notification
  // - refreshNotifications: manual refresh
  // - getPreview/getDrawerSlice: UI slicing
  // - resortNotifications: re-sort on demand
  // ============================================================
  return {
    notifications, // üì¶ All notifications, sorted (unread first)
    unreadCount, // üõéÔ∏è Badge: how many unread
    loading, // ‚è≥ Loading spinner state
    markAsRead, // ‚úÖ Mark as read function (optimistic)
    refreshNotifications, // üîÅ Manual refresh
    removeNotification, // üóëÔ∏è For single
    clearAllNotifications, // üî• For all
    getPreview, // üîç Preview top n notifications
    getDrawerSlice, // üìÑ Drawer/page slicing
    resortNotifications // üîÄ Re-sort helper
  };
}
