  // 9ï¸âƒ£ FreeTrial helpers
  //    â€¢ requestFreeTrials: ask for user's trial data
  //    â€¢ subscribeFreeTrials: listen for trial updates
  // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
  const requestFreeTrials = () => emit('fetch_free_trials');
  const subscribeFreeTrials = (handler) => listen('freeTrials_update', handler);

